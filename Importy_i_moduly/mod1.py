
def dodawanie():
    a = int(input("Podaj pierwszą liczbę: "))
    b = int(input("Podaj drugą liczbę: "))
    wynik = a + b
    print(a, "+", b, "=", wynik)

def odejmowanie():
    a = int(input("Podaj pierwszą liczbę: "))
    b = int(input("Podaj drugą liczbę: "))
    wynik = a - b
    print(a, "-", b, "=", wynik)

def mnozenie():
    a = int(input("Podaj pierwszą liczbę: "))
    b = int(input("Podaj drugą liczbę: "))
    wynik = a * b
    print(a, "*", b, "=", wynik)

def dzielenie():
    a = int(input("Podaj pierwszą liczbę: "))
    b = int(input("Podaj drugą liczbę: "))
    wynik = a / b
    print(a, ":", b, "=", wynik)
